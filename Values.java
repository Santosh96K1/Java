public class Values {
    static int a;
    static double b;
    static float c;
    static boolean d;
    static char e;
    static byte f;
    static short g;
    static long h;
    static String i;

    public static void main(String[] args) {
        System.out.println("intVar: " + a);
        System.out.println("doubleVar: " + b);
        System.out.println("floatVar: " + c);
        System.out.println("booleanVar: " + d);
        System.out.println("charVar: " + e); 
        System.out.println("byteVar: " + f);
        System.out.println("shortVar: " + g);
        System.out.println("longVar: " + h);
        System.out.println("stringVar: " + i);
    }
}
